void TestFunc_CLK_EnableSysTick()
{
    uint32_t u32TickCount[4] = {0, 0x555555, 0xAAAAAA, 0xFFFFFF};

    uint8_t u8SysTickClkSrcSelIdx;
    uint8_t u8TickCountIdx;

    /* Test loop */
    for (u8SysTickClkSrcSelIdx = 0; u8SysTickClkSrcSelIdx < (sizeof(au32SysTickClkSrcSel) / sizeof(uint32_t)); u8SysTickClkSrcSelIdx++)
    {
        for (u8TickCountIdx = 0; u8TickCountIdx < 4; u8TickCountIdx++)
        {
            /* Enable Systick, clock source from external clock */
            CLK_EnableSysTick(au32SysTickClkSrcSel[u8SysTickClkSrcSelIdx], u32TickCount[u8TickCountIdx]);
            CU_ASSERT((CLK->STSEL & CLK_STSEL_ST0SEL_Msk) == au32SysTickClkSrcSel[u8SysTickClkSrcSelIdx]);
            CU_ASSERT((SysTick->CTRL & (SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk)) == (SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk));
            CU_ASSERT((SysTick->CTRL & SysTick_CTRL_CLKSOURCE_Msk) == 0);
            CU_ASSERT(SysTick->LOAD == u32TickCount[u8TickCountIdx]);

            /* Disable Systick */
            CLK_DisableSysTick();
            CU_ASSERT(SysTick->CTRL == 0);

            /* Enable Systick, clock source from ACLK(core clock) */
            CLK_EnableSysTick(CLK_STSEL_ACLK, u32TickCount[u8TickCountIdx]);
            CU_ASSERT((SysTick->CTRL & (SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk)) == (SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk));
            CU_ASSERT((SysTick->CTRL & SysTick_CTRL_CLKSOURCE_Msk) == SysTick_CTRL_CLKSOURCE_Msk);
            CU_ASSERT(SysTick->LOAD == u32TickCount[u8TickCountIdx]);

            /* Disable Systick */
            CLK_DisableSysTick();
            CU_ASSERT(SysTick->CTRL == 0);
        }
    }    
}
