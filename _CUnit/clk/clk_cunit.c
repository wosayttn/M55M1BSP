void TestFunc_CLK_EnableCKO()
{
    uint8_t u8TestClkSrc;   //CLKO cloclk source
    uint8_t u8TestFreqDiv;  //CLKO frequency divider

    /* Test loop */
    /* Select CLKO cloclk source */
    for (u8TestClkSrc = 0; u8TestClkSrc < (sizeof(au32CLKOClkSrcSel) / sizeof(uint32_t)); u8TestClkSrc++)
    {
        //select CLKO frequency divider
        for (u8TestFreqDiv = 0; u8TestFreqDiv < 16; u8TestFreqDiv++)
        {
            //test function: enable CKO
            CLK_EnableCKO(au32CLKOClkSrcSel[u8TestClkSrc], u8TestFreqDiv, 1);
            //check result
            CU_ASSERT((CLK->CLKOSEL & CLK_CLKOSEL_CLKOSEL_Msk) == au32CLKOClkSrcSel[u8TestClkSrc]);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_CLKOEN_Msk) == CLK_CLKOCTL_CLKOEN_Msk);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_FREQSEL_Msk) == u8TestFreqDiv);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_DIV1EN_Msk) == CLK_CLKOCTL_DIV1EN_Msk);

            //test function: disable CKO clock
            CLK_DisableCKO();

            //check result
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_CLKOEN_Msk) == 0);

            //test function: enable CKO
            CLK_EnableCKO(au32CLKOClkSrcSel[u8TestClkSrc], u8TestFreqDiv, 0);

            //check result
            CU_ASSERT((CLK->CLKOSEL & CLK_CLKOSEL_CLKOSEL_Msk) == au32CLKOClkSrcSel[u8TestClkSrc]);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_CLKOEN_Msk) == CLK_CLKOCTL_CLKOEN_Msk);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_FREQSEL_Msk) == u8TestFreqDiv);
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_DIV1EN_Msk) == 0);

            //test function: disable CKO clock
            CLK_DisableCKO();

            //check result
            CU_ASSERT((CLK->CLKOCTL & CLK_CLKOCTL_CLKOEN_Msk) == 0);
        }
    }
}

