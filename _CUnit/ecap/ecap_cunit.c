uint32_t CAP_ENPIN[2][3] = {0};
#define C0PIN 0
#define C1PIN 1
#define C2PIN 2

volatile uint32_t  *au32EcapPin[12] =
{
    &PA10, &PA9, &PA8,
    &PC10, &PC11, &PC12,
    &PD1, &PD0, &PD13,
    &PA12, &PA13, &PA14,
};

void SysTick_Handler(void)
{
    if (CAP_ENPIN[0][C0PIN] == 1)
    {
        *au32EcapPin[C0PIN] ^= 1;
    }

    if (CAP_ENPIN[0][C1PIN] == 1)
    {
        *au32EcapPin[C1PIN] ^= 1;
    }

    if (CAP_ENPIN[0][C2PIN] == 1)
    {
        *au32EcapPin[C2PIN] ^= 1;;
    }
}

