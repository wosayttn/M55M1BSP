void MACRO_Noise_Filter(void)
{
    volatile uint32_t i;

    SYS_ResetModule(SYS_ECAP0RST);
    SYS_ResetModule(SYS_ECAP1RST);
    SYS_ResetModule(SYS_ECAP2RST);
    SYS_ResetModule(SYS_ECAP3RST);

    for (i = 0; i < 4; i++)
    {
        ECAP_ENABLE_CNT(ECAPCh[i]);

        /* Enable and Select CAP_CLK/1*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_1);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_1);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
        /* Enable and Select CAP_CLK/2*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_2);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_2);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
        /* Enable and Select CAP_CLK/4*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_4);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_4);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
        /* Enable and Select CAP_CLK/16*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_16);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_16);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
        /* Enable and Select CAP_CLK/32*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_32);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_32);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
        /* Enable and Select CAP_CLK/64*/
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_64);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == 0);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_64);
        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);

        /* Enable Capture Timer/Counter */
        ECAP_NOISE_FILTER_ENABLE(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_1);
        /* Select CAP_CLK/64*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_64);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_64);
        /* Select CAP_CLK/32*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_32);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_32);
        /* Select CAP_CLK/16*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_16);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_16);
        /* Select CAP_CLK/4*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_4);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_4);
        /* Select CAP_CLK/2*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_2);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_2);
        /* Select CAP_CLK/1*/
        ECAP_SET_NOISE_FILTER_CLKDIV(ECAPCh[i], ECAP_NOISE_FILTER_CLKDIV_1);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_NFCLKSEL_Msk) == ECAP_NOISE_FILTER_CLKDIV_1);

        ECAP_NOISE_FILTER_DISABLE(ECAPCh[i]);
        CU_ASSERT((ECAPCh[i]->CTL0 & ECAP_CTL0_CAPNFDIS_Msk) == ECAP_CTL0_CAPNFDIS_Msk);
    }
}

void MACRO_Input_Channel(void)
{
    volatile uint32_t i;

    SYS_ResetModule(SYS_ECAP0RST);
    SYS_ResetModule(SYS_ECAP1RST);
    SYS_ResetModule(SYS_ECAP2RST);
    SYS_ResetModule(SYS_ECAP3RST);

    for (i = 0; i < 4; i++)
    {
        ECAP_ENABLE_CNT(ECAPCh[i]);

        /* Enable IC0 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC0EN_Msk), ECAP_CTL0_IC0EN_Msk);
        /* Disable IC0 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC0EN_Msk), 0);

        /* Enable IC1 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC1EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC1EN_Msk), ECAP_CTL0_IC1EN_Msk);
        /* Disable IC1 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC1EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC1EN_Msk), 0);

        /* Enable IC2 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC2EN_Msk), ECAP_CTL0_IC2EN_Msk);
        /* Disable IC2 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & ECAP_CTL0_IC2EN_Msk), 0);

        /* Enable IC0, IC1 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk)), (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk));
        /* Disable IC0, IC1 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk)), 0);

        /* Enable IC0, IC2 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC2EN_Msk)), (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC2EN_Msk));
        /* Disable IC0, IC2 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC2EN_Msk)), 0);

        /* Enable IC1, IC2 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk)), (ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk));
        /* Disable IC1, IC2 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk)), 0);

        /* Enable IC0, IC1, IC2 */
        ECAP_ENABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk)), (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk));
        /* Disable IC0, IC1, IC2 */
        ECAP_DISABLE_INPUT_CHANNEL(ECAPCh[i], ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk);
        CU_ASSERT_EQUAL((ECAPCh[i]->CTL0 & (ECAP_CTL0_IC0EN_Msk | ECAP_CTL0_IC1EN_Msk | ECAP_CTL0_IC2EN_Msk)), 0);


        ECAP_DISABLE_CNT(ECAPCh[i]);
    }
}

